# Faça um algoritmo que receba dois números, calcule a divisão, a multiplicação, a soma, a diferença entre
# eles e mostre os resultados.

primeiro_numero = float(input("Digite o primeiro número: "))
segundo_numero = float(input("Digite o segundo número: "))

divisao_numero = primeiro_numero / segundo_numero
multiplicacao_numero = primeiro_numero * segundo_numero
soma_numero = primeiro_numero + segundo_numero
diferenca_numero = primeiro_numero - segundo_numero

print("O resultado da divisão é: ", divisao_numero)
print("O resultado da multiplicação é: ", multiplicacao_numero)
print("O resultado da soma é: ", soma_numero)
print("O resultado da diferença é: ", diferenca_numero)

