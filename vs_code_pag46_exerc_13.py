# Escreva um algoritmo que leia dois números, calcule o produto entre eles, mostre o resultado e os 
# números recebidos.

primeiro_numero = float(input("Digite o primeiro número: "))
segundo_numero = float(input("Digite o segundo número: "))
resultado = primeiro_numero * segundo_numero

print("O resultado da multilicação é:", resultado) 
print("O primeiro número é:", primeiro_numero, "e o segundo número é:", segundo_numero)
