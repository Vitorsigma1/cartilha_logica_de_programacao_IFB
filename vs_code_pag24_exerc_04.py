# Ler quatro números quaisquer e mostrar o resultado da média destes números.
primeiro_numero = float(input("Digite um número: "))
segundo_numero = float(input("Digite outro número: "))
terceiro_numero = float(input("Digite mais um número: "))
quarto_numero = float(input("Digite novamento um número: "))
somar = primeiro_numero + segundo_numero + terceiro_numero + quarto_numero
media = somar / 4
print("A média dos números digitados é: ", media)
