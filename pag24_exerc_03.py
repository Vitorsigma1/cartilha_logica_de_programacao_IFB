# Ler três números quaisquer e mostrar o resultado da soma destes números.
primeiro_numero = float(input("Digite um número qualquer: "))
segundo_numero = float(input("Digite mais um número qualquer: "))
terceiro_numero = float(input("Digite novamente um número qualquer: "))
soma = primeiro_numero + segundo_numero + terceiro_numero
print(f"O valor da soma dos números é: {soma:.2f}")
