# Mostrar se um número qualquer é positivo, ou negativo, ou zero.

numero_qualquer = float(input("Digite um número qualquer: "))

if numero_qualquer > 0:
    print("O número", numero_qualquer, "é positivo!")

elif numero_qualquer < 0:
    print("O número", numero_qualquer, "é negativo!")

elif numero_qualquer == 0:
    print("O número é zero!" )


    
