# Faça um algoritmo que leia uma temperatura fornecida em graus Fahrenheit e
# converta para seus equivalente em graus centígrados.

graus_fahrenheit = float(input("Digite a temperatura em graus Fahrenheit: "))

graus_centigrados = 5/9 * (graus_fahrenheit - 32)

print(f"A temperatura em graus Fahrenheit é: {graus_fahrenheit:.2f}, convertendo para graus Centígrados temos: {graus_centigrados:.2f}")

                         
